while True:
 a = input(str('Masukan Warna : '))

 if a == ('merah') :
    print('Red')
 elif a == ('biru') :
    print('Blue')  
 elif a == ('kuning') :
    print('Yellow')
 elif a == ('hitam') :
    print('Black')
 elif a == ('putih') :
    print('White')
 elif a == ('merah muda') :
    print('Pink')
 elif a == ('hijau') :
    print('Green')
 else:
    print('Warna Kang')